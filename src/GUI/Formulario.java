    private void botoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoActionPerformed
        SQL cc = new SQL();
        Connection cn = cc.conectar();
        if (cn != null) {
            String nom, apell, CorreoE,usu, contra;
            int Telf;
            String sql = " ";
            nom = text1.getText();
            apell = txt2.getText();
            CorreoE = txt3.getText();
            Telf = Integer.parseInt(txt4.getText());
            usu = txt5.getText();
            contra = txt7.getText();
            sql = "INSERT INTO persona(nombre,apellido,correoE,telefono,usuario,contrasenia) VALUES (?,?,?,?,?,?)";
            try {
                PreparedStatement pst = cn.prepareStatement(sql);
                pst.setString(1, nom);
                pst.setString(2, apell);
                pst.setString(3, CorreoE);
                pst.setInt(4, Telf);
                pst.setString(5, usu);
                pst.setString(6, contra);
                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "Se ha guardado con √©xito");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                // Manejo del error, muestra un mensaje de error si es necesario.
            }
        } else {
            // Manejo si la conexi√≥n es nula, muestra un mensaje de error por ejemplo.
            JOptionPane.showMessageDialog(null, "Error en la conexi√≥n a la base de datos");
        }
    }//GEN-LAST:event_botoActionPerformed

